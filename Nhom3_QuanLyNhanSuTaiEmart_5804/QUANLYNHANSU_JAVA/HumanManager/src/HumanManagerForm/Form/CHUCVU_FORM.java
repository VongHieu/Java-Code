    public void Loadulieu() {

        try {
            tblchucvu.removeAll();
            String[] arr = {"Mã chức vụ", "Tên Chức vụ"};
            tbtModel = new DefaultTableModel(arr, 0);
            String sql = "SELECT * FROM CHUCVU";
            Connection conn = DatabaseConnection.openConnection();
            PreparedStatement pm = conn.prepareStatement(sql);
            ResultSet rs = pm.executeQuery();
            tbtModel.setRowCount(0);
            while (rs.next()) {
                String[] row = new String[]{
                    rs.getString("MACV"), rs.getString("TENCV")};
                tbtModel.addRow(row);
            }
            tblchucvu.setModel(tbtModel);
            tbtModel.fireTableDataChanged();
            tren = tblchucvu.getRowCount();
            lblDong.setText(duoi + "/" + tren);
            DatabaseConnection.CloseConnection(conn);
        } catch (Exception e) {
            e.printStackTrace();
        }

    }

