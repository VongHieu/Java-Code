    private void btncnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncnActionPerformed
        // TODO add your handling code here:
        StringBuilder sb = new StringBuilder();
        if (txtmacv.getText().equals("")) {
            sb.append("ID không được để trống!\n");
            txtmacv.requestFocus();
        }
        if (txttenccv.getText().equals("")) {
            sb.append("Mật khẩu không được để trống!\n");
        }
        if (sb.length() > 0) {
            JOptionPane.showMessageDialog(this, sb.toString(), "Invalidation", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try {
            ChucVu cv = new ChucVu();
            cv.setMaCV(txtmacv.getText());
            cv.setTenCV(txttenccv.getText());
            ChucVuDAO tk = new ChucVuDAO();
            tk.Update(cv);
            JOptionPane.showMessageDialog(this, "Cập nhật thông tin thành công!");
            Loadulieu();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Cập nhật không thành công!");
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_btncnActionPerformed

    private void btntimkiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btntimkiemActionPerformed
        // TODO add your handling code here:
        StringBuilder sb = new StringBuilder();
        if (txttimkiem.getText().equals("")) {
            sb.append("Không được để trống!\n");
            txttimkiem.requestFocus();
        }
        if (sb.length() > 0) {
            JOptionPane.showMessageDialog(this, sb.toString(), "Invalidation", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try {
            ChucVuDAO cv = new ChucVuDAO();
            ChucVu CV = cv.Search(txttimkiem.getText());
            if (CV != null) {
                txtmacv.setText(CV.getMaCV());
                txttenccv.setText(CV.getTenCV());
            } else {
                JOptionPane.showMessageDialog(this, "Chức vụ không được tìm thấy!");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());

        }
    }//GEN-LAST:event_btntimkiemActionPerformed
    private void SuKien() {
        txtmacv.setText(tblchucvu.getValueAt(duoi - 1, 0).toString());
        txttenccv.setText(tblchucvu.getValueAt(duoi - 1, 1).toString());
    }
    private void btncuoiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncuoiActionPerformed
        // TODO add your handling code here:
        tblchucvu.setRowSelectionInterval(tren - 1, tren - 1);
        duoi = tren;
        lblDong.setText(duoi + "/" + tren);
        SuKien();
    }//GEN-LAST:event_btncuoiActionPerformed

    private void btnsauActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsauActionPerformed
        // TODO add your handling code here:
        if (duoi + 1 > tren) {
            return;
        }
        duoi += 1;
        lblDong.setText(duoi + "/" + tren);
        tblchucvu.setRowSelectionInterval(duoi - 1, duoi - 1);
        SuKien();
    }//GEN-LAST:event_btnsauActionPerformed

    private void btntruocActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btntruocActionPerformed
        // TODO add your handling code here:
        if (duoi - 1 < 1) {
            return;
        }
        duoi -= 1;
        lblDong.setText(duoi + "/" + tren);
        tblchucvu.setRowSelectionInterval(duoi - 1, duoi - 1);
        SuKien();
    }//GEN-LAST:event_btntruocActionPerformed

    private void btndauActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndauActionPerformed
        // TODO add your handling code here:
        tblchucvu.setRowSelectionInterval(0, 0);
        duoi = 1;
        lblDong.setText(duoi + "/" + tren);
        SuKien();
    }//GEN-LAST:event_btndauActionPerformed

