    private void btnTKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTKActionPerformed
        // TODO add your handling code here:
        StringBuilder sb = new StringBuilder();
        if (txtTimkiem.getText().equals("")) {
            sb.append("Không được để trống!\n");
            txtTimkiem.requestFocus();
        }
        if (sb.length() > 0) {
            JOptionPane.showMessageDialog(this, sb.toString(), "Invalidation", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try {
            NhanVienDao NV = new NhanVienDao();
            NhanVien nv = NV.Search(txtTimkiem.getText());
            if (nv != null) {
                txtMaNV.setText(nv.getMANV());
                txtTennv.setText(nv.getTENNV());
                txtngaysinh.setText(nv.getNgaySinh());
                txtGioitinh.setText(nv.getGIOITINH());
                txtDiachi.setText(nv.getDIACHI());
                txtLCB.setText(nv.getDIENTHOAI());
                txtEmail.setText(nv.getEMAIL());
                txtSDT.setText("" + nv.getLUONGCOBAN());
                txtPB.setText("" + nv.getNGUOIPHUTHUOC());
                txtPhucap.setText(nv.getMaPB());
                txtMaCVu.setText(nv.getMaCV());
                txtThue.setText(nv.getMAPHUCAP());
                txtNguoiPT.setText(nv.getMaThue());

            } else {
                JOptionPane.showMessageDialog(this, "Không tìm thấy nhân viên!");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());

        }
    }//GEN-LAST:event_btnTKActionPerformed

