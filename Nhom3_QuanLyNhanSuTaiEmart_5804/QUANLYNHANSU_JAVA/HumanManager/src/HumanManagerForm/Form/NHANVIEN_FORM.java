    public void Loadulieu() {

        try {
            tblNhanvien.removeAll();
            String[] arr = {"Mã NV", "Tên NV", "Ngày sinh", "Phái", "Địa chỉ", "SDT", "Email", "Lương CB", "Người PT", "Mã PB", "Mã CV", "Mã PC", "Mã Thuế"};
            tableModel = new DefaultTableModel(arr, 0);
            String sql = "SELECT * FROM NHANVIEN";
            Connection conn = DatabaseConnection.openConnection();
            PreparedStatement pm = conn.prepareStatement(sql);
            ResultSet rs = pm.executeQuery();
            tableModel.setRowCount(0);
            while (rs.next()) {
                String[] row = new String[]{
                    rs.getString("MANV"), rs.getString("HOTENNV"), rs.getString("NGAYSINH"), rs.getString("GIOITINH"), rs.getString("DIACHI"), rs.getString("SODIENTHOAI"), rs.getString("EMAIL"),
                    rs.getString("LUONGCOBAN"), rs.getString("NGUOIPHUTHUOC"), rs.getString("MAPB"), rs.getString("MACV"), rs.getString("MAPHUCAP"), rs.getString("MATHUE")};
                tableModel.addRow(row);
            }
            tblNhanvien.setModel(tableModel);
            tableModel.fireTableDataChanged();
            tren = tblNhanvien.getRowCount();
            lblDong.setText(duoi + "/" + tren);
            DatabaseConnection.CloseConnection(conn);
        } catch (Exception e) {
            e.printStackTrace();
        }

    }

