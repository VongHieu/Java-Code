    public void Loadulieu() {

        try {
            tblbc.removeAll();
            String[] arr = {"Mã BC", "Mã NV", "Tên BC", "Loại BC", "Ngày cấp", "Đơn vị cấp"};
            tbtModel = new DefaultTableModel(arr, 0);
            String sql = "SELECT * FROM BANGCAP";
            Connection conn = DatabaseConnection.openConnection();
            PreparedStatement pm = conn.prepareStatement(sql);
            ResultSet rs = pm.executeQuery();
            tbtModel.setRowCount(0);
            while (rs.next()) {
                String[] row = new String[]{
                    rs.getString("MABC"), rs.getString("MANV"), rs.getString("TENBC"), rs.getString("LOAIBC"),
                    rs.getString("NGAYCAP"), rs.getString("DONVICAP")};
                tbtModel.addRow(row);
            }
            tblbc.setModel(tbtModel);
            tbtModel.fireTableDataChanged();
            tren = tblbc.getRowCount();
            lblDong.setText(duoi + "/" + tren);
            DatabaseConnection.CloseConnection(conn);
        } catch (Exception e) {
            e.printStackTrace();
        }

    }

