    private void btntimkiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btntimkiemActionPerformed
        // TODO add your handling code here:
        StringBuilder sb = new StringBuilder();
        if (txttimkiem.getText().equals("")) {
            sb.append("Không được để trống!\n");
            txttimkiem.requestFocus();
        }
        if (sb.length() > 0) {
            JOptionPane.showMessageDialog(this, sb.toString(), "Invalidation", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try {
            BangCapDAO pb = new BangCapDAO();
            BangCap PB = pb.Search(txttimkiem.getText());
            if (PB != null) {
                txtmabc.setText(PB.getMABC());
                txtmanv.setText(PB.getMANV());
                txttenbc.setText(PB.getTENBC());
                txtloaibc.setText(PB.getLOAIBC());
                txtngaycap.setText(PB.getNGAYCAP());
                txtdvcap.setText(PB.getDONVICAP());

            } else {
                JOptionPane.showMessageDialog(this, "Mã bằng cấp không được tìm thấy!");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());

        }
    }//GEN-LAST:event_btntimkiemActionPerformed

    private void tblbcMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblbcMouseClicked
        // TODO add your handling code here:
        int row = tblbc.getSelectedRow();
        if (row >= 0) {
            duoi = row + 1;
            lblDong.setText(duoi + "/" + tren);
            txtmabc.setText(tblbc.getValueAt(row, 0).toString());
            txtmanv.setText(tblbc.getValueAt(row, 1).toString());
            txttenbc.setText(tblbc.getValueAt(row, 2).toString());
            txtloaibc.setText(tblbc.getValueAt(row, 3).toString());
            txtngaycap.setText(tblbc.getValueAt(row, 4).toString());
            txtdvcap.setText(tblbc.getValueAt(row, 5).toString());
        }
    }//GEN-LAST:event_tblbcMouseClicked

